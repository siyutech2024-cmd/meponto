function normalizePhrase(phrase: string) {
  return phrase.trim().replace(/\s+/g, " ");
}

function toEnglishPhrase(phrase: string) {
  const normalized = normalizePhrase(phrase);

  for (const translations of Object.values(phraseDictionary)) {
    for (const [english, translated] of Object.entries(translations)) {
      if (normalizePhrase(translated) === normalized) {
        return english;
      }
    }
  }

  return normalized;
}

export function translatePhrase(language: Language, phrase: string) {
  const leading = phrase.match(/^\s*/)?.[0] ?? "";
  const trailing = phrase.match(/\s*$/)?.[0] ?? "";
  const normalized = normalizePhrase(phrase);

  if (!normalized) {
    return phrase;
  }

  const englishPhrase = toEnglishPhrase(normalized);

  if (language === "en") {
    return `${leading}${englishPhrase}${trailing}`;
  }

  const translated = phraseDictionary[language][englishPhrase] ?? phraseDictionary[language][normalized];
  if (translated) {
    return `${leading}${translated}${trailing}`;
  }

  const demoCode = normalized.match(/^Verification code sent\. Demo code: (.+)$/);
  if (demoCode) {
    return language === "zh"
      ? `${leading}验证码已发送。演示码：${demoCode[1]}${trailing}`
      : `${leading}Código de verificação enviado. Código demo: ${demoCode[1]}${trailing}`;
  }

  const target = normalized.match(/^Target (.+)$/);
  if (target) {
    return language === "zh" ? `${leading}目标 ${target[1]}${trailing}` : `${leading}Meta ${target[1]}${trailing}`;
  }

  const online = normalized.match(/^(.+) online$/);
  if (online) {
    return language === "zh" ? `${leading}${online[1]} 在线${trailing}` : `${leading}${online[1]} online${trailing}`;
  }

  return phrase;
}
